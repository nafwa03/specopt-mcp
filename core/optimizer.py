def _create_teleprompter(optimizer_type: str, metric: Callable, lm_client,
                         task_model=None, **kwargs) -> object:
    """Factory: build and return a configured teleprompter (MIPROv2 or GEPA)."""
    ot = optimizer_type.lower().strip()

    if ot == "mipro":
        return MIPROv2(
            metric=metric,
            prompt_model=task_model,
            task_model=task_model,
            num_candidates=kwargs.get("num_candidates", ConfigLoader.get("pipeline", "mipro", "num_candidates")),
            auto=kwargs.get("auto", None),
            seed=kwargs.get("seed", ConfigLoader.get("pipeline", "mipro", "seed")),
        )

    elif ot == "gepa":
        gepa_cfg = ConfigLoader.get("pipeline", "gepa")
        gepa_kwargs = dict(
            metric=_adapt_metric_for_gepa(metric),
            reflection_lm=lm_client,
            seed=kwargs.get("seed", gepa_cfg.get("seed", 0)),
            num_threads=gepa_cfg.get("num_threads", 1),
        )
        if "max_metric_calls" in gepa_cfg:
            gepa_kwargs["max_metric_calls"] = gepa_cfg["max_metric_calls"]
            gepa_kwargs["auto"] = None
        else:
            gepa_kwargs["auto"] = kwargs.get("auto", gepa_cfg.get("auto", "light"))
        return GEPA(**gepa_kwargs)

    else:
        raise ValueError(f"Unknown optimizer_type '{optimizer_type}'. Must be 'mipro' or 'gepa'.")


def _extract_optimized_instructions(teleprompter, compiled_program, optimizer_type: str) -> str:
    """Extract the optimized instruction text from a compiled program.
    Handles both MIPROv2 and GEPA storage paths."""
    for attr_path in [
        lambda p: p.signature.instructions,
        lambda p: p.program.signature.instructions,
        lambda p: p.predictor.signature.instructions,
        lambda p: p.program.predictor.signature.instructions,
    ]:
        try:
            val = attr_path(compiled_program)
            if val and val.strip():
                return val
        except (AttributeError, TypeError):
            continue

    if hasattr(teleprompter, 'instruction_history') and teleprompter.instruction_history:
        last = teleprompter.instruction_history[-1]
        if last and last.strip():
            return last

    logger.warning("Could not extract optimized instructions from compiled %s program", optimizer_type)
    return str(compiled_program)


def _extract_optimized_demos(compiled_program, optimizer_type: str) -> list:
    """Extract demos from a compiled program. GEPA has none."""
    if optimizer_type.lower().strip() == "gepa":
        return []
    return getattr(compiled_program, 'demos', [])


