def run_dataset_generation_pipeline(agent_markdown_path: str, provider: str = "lm-studio", model: str = "",
                                    lm_client=None, registry=None, num_examples: int = 5,
                                    document_dir: str = "", curate: bool = False,
                                    curation_threshold: float = 0.0,
                                    output_format: str = "json") -> str:
    """Generate a synthetic training dataset from an agent markdown file without running optimization.

    Supports chunking for large docs, LLM-as-judge curation, and multiple output formats.
    Optionally accepts a document_dir for supplementary reference documentation."""
