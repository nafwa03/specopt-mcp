class SpecSectionSignature(dspy.Signature):
    raw_section = dspy.InputField(desc="The original raw markdown section text including its header.")
    optimized_section = dspy.OutputField(
        desc="The revised, clarified, and optimized markdown section. Preserve all code blocks and key facts.")


SpecSectionSignature.__doc__ = PromptLoader.get("signatures", "SpecSectionSignature")


class SpecSectionQualitySignature(dspy.Signature):
    raw_section = dspy.InputField(desc="The original unmodified section text.")
    optimized_section = dspy.InputField(desc="The optimized version of the section.")
    quality_score = dspy.OutputField(desc="An integer from 0 to 100 indicating how much the optimized version improves quality.")


SpecSectionQualitySignature.__doc__ = PromptLoader.get("signatures", "SpecSectionQualitySignature")


# =====================================================================
# SECTION UTILITIES
# =====================================================================

def extract_sections_from_markdown(file_path: str) -> list:
    """Parse a markdown file into sections by ## headers.
    Returns list of dicts with keys: header, body, full_text.
    The first section (before any ##) is treated as the document overview.
    """
    with open(file_path, "r", encoding="utf-8") as f:
        content = f.read()

    sections = []
    parts = re.split(r'\n(?=## )', content)

    for i, part in enumerate(parts):
        part = part.strip()
        if not part:
            continue

        lines = part.split('\n')
        header_line = lines[0] if lines else ""

        if header_line.startswith('## '):
            header = header_line
            body = '\n'.join(lines[1:]).strip()
        elif i == 0:
            header = "# Overview"
            body = part
        else:
            header = "# Section"
            body = part

        if len(body) >= ConfigLoader.get("pipeline", "section", "min_section_length"):
            sections.append({
                "header": header,
                "body": body,
                "full_text": part,
                "index": i
            })

    return sections


def compute_text_similarity(original: str, optimized: str) -> float:
    """Compute a similarity ratio between 0.0 and 1.0 using difflib sequence matcher."""
    return difflib.SequenceMatcher(None, original, optimized).ratio()


# =====================================================================
# SECTION OPTIMIZATION PIPELINE
# =====================================================================

def run_section_optimization_pipeline(agents_markdown_path: str, provider: str = "lm-studio",
                                      model: str = "", lm_client=None, registry=None) -> str:
    """Parse an AGENTS markdown file into sections, optimize each independently,
    and output a side-by-side original-vs-optimized comparison document.
    """
    if not os.path.exists(agents_markdown_path):
        return f"Error: Target file not found at path: {agents_markdown_path}"

    try:
        if lm_client is None:
            lm_client = _get_lm_client(provider, model, registry)

        sections = extract_sections_from_markdown(agents_markdown_path)
        logger.info(f"Extracted {len(sections)} sections from {agents_markdown_path}")

        comparison_lines = []
        optimized_blocks = []

        with dspy.context(lm=lm_client):
            section_optimizer = dspy.Predict(SpecSectionSignature)
            quality_judge = dspy.Predict(SpecSectionQualitySignature)

            for sec in sections:
                header = sec["header"]
                body = sec["body"]
                full = sec["full_text"]
                logger.info(f"Processing section: {header}")

                # 1. Score the original
                try:
                    baseline_judge = quality_judge(
                        raw_section=full,
                        optimized_section=full
                    )
                    baseline_score = int(re.search(r'\d+', str(baseline_judge.quality_score)).group())
                except Exception:
                    baseline_score = ConfigLoader.get("pipeline", "section", "default_baseline_score")

                # 2. Optimize the section
                try:
                    optimized_result = section_optimizer(raw_section=full)
                    optimized_text = optimized_result.optimized_section.strip()
                except Exception as e:
                    logger.warning(f"Optimization failed for {header}: {e}")
                    optimized_text = body

                # 3. Score the optimized version
                try:
                    scored = quality_judge(
                        raw_section=full,
                        optimized_section=optimized_text
                    )
                    optimized_score = int(re.search(r'\d+', str(scored.quality_score)).group())
                except Exception:
                    optimized_score = baseline_score

                # 4. Compute edit distance ratio
                similarity = compute_text_similarity(body, optimized_text)
                edit_pct = int((1.0 - similarity) * 100)

                # Use the higher of judge score and edit distance for the displayed percentage
                display_pct = max(optimized_score - baseline_score, edit_pct)
                display_pct = max(0, min(100, display_pct))

                change_note = ""
                if display_pct <= ConfigLoader.get("pipeline", "section", "no_change_threshold"):
                    change_note = "No change"

                # Reconstruct optimized block for full-doc output.
                # The LM may return the header inside optimized_text, so strip any leading
                # header match to avoid duplicating it.
                if optimized_text.startswith(header):
                    optimized_body = optimized_text[len(header):].strip()
                else:
                    optimized_body = optimized_text
                reconstructed = f"{header}\n{optimized_body}"
                optimized_blocks.append(reconstructed)

                # Build comparison block
                comparison_lines.append(f"{header}")
                comparison_lines.append("-- ORIGINAL")
                comparison_lines.append(body)
                comparison_lines.append("")
                if change_note:
                    comparison_lines.append(f"-- OPTIMIZED ({display_pct}%) {change_note}")
                else:
                    comparison_lines.append(f"-- OPTIMIZED ({display_pct}%)")
                comparison_lines.append(optimized_text)
                comparison_lines.append("")

        output = "\n".join(comparison_lines)
        reconstructed_doc = "\n\n".join(optimized_blocks)

        output_dir = os.path.dirname(os.path.abspath(agents_markdown_path))
        base_name = os.path.splitext(os.path.basename(agents_markdown_path))[0]
        suffixes = ConfigLoader.get("file_suffixes")

        try:
            comparison_path = os.path.join(output_dir, f"{base_name}{suffixes['section_comparison']}")
            with open(comparison_path, "w", encoding="utf-8") as f:
                f.write(output)
            logger.info(f"Section comparison saved to {comparison_path}")
        except Exception as e:
            logger.error(f"Failed to save comparison output: {e}")

        try:
            optimized_path = os.path.join(output_dir, f"{base_name}{suffixes['optimized']}.md")
            with open(optimized_path, "w", encoding="utf-8") as f:
                f.write(reconstructed_doc)
            logger.info(f"Reconstructed optimized document saved to {optimized_path}")
        except Exception as e:
            logger.error(f"Failed to save reconstructed optimized doc: {e}")

        return output

    except Exception as e:
        logger.exception("Section optimization pipeline failed:")
        return f"Section optimization pipeline failed: {str(e)}"
