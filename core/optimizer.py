def secure_universal_llm_metric(gold, pred, trace=None):
    """A multi-stage secure evaluation metric that prevents prompt injection and false info."""
    pred_text = getattr(pred, 'output_response', str(pred) if pred else "").strip()
    gold_text = getattr(gold, 'output_response', "").strip()

    if not pred_text or len(pred_text) < 3:
        logger.warning("Metric: rejected trivial/empty prediction (len=%d)", len(pred_text))
        return 0.0

    if pred_text == gold_text:
        logger.warning("Metric: rejected exact gold match (echoing)")
        return 0.0

    try:
        if trace is not None and hasattr(trace, 'current_instruction'):
            candidate_prompt = trace.current_instruction
            security_auditor = dspy.Predict(SecurityAuditorSignature)
            security_check = security_auditor(proposed_prompt=candidate_prompt)
            if "YES" in security_check.injection_vulnerability_detected.upper():
                logger.warning(
                    "Security Guard Triggered: Mutation rejected - prompt injection vulnerability risk detected.")
                return 0.0
    except Exception:
        logger.warning("Security auditor LM call failed, defaulting to 0.0")
        return 0.0

    try:
        fact_auditor = dspy.Predict(FactGroundingAuditorSignature)
        fact_check = fact_auditor(
            gold_reference=gold_text,
            agent_prediction=pred_text
        )
        if "YES" in fact_check.contains_false_information.upper():
            logger.warning("Hallucination Guard Triggered: Variant rejected - false claims found.")
            return 0.0
    except Exception:
        logger.warning("Fact auditor LM call failed, defaulting to 0.0")
        return 0.0

    try:
        judge = dspy.Predict(UniversalJudgeSignature)
        result = judge(
            input_context=gold.input_context,
            gold_response=gold_text,
            agent_prediction=pred_text
        )
        return 1.0 if "YES" in result.verdict.upper() else 0.0
    except Exception:
        logger.warning("Judge LM call failed, defaulting to 0.0")
        return 0.0


def universal_llm_metric(gold_example, agent_prediction, trace=None) -> float:
    """Simpler judge metric using contextual judge. Kept for backward compatibility."""
    gold_text = gold_example.gold_response
    pred_text = getattr(agent_prediction, 'output_response', str(agent_prediction))
    input_text = gold_example.input_context
    associated_context = getattr(gold_example, 'source_context', "")

    judge = dspy.Predict(ContextualJudgeSignature)
    try:
        response = judge(
            source_documentation=associated_context,
            input_context=input_text,
            gold_response=gold_text,
            agent_prediction=pred_text
        )
        verdict = response.verdict.strip().upper()
        return 1.0 if re.search(r"\bYES\b", verdict) else 0.0
    except Exception as e:
        logger.error(f"Universal judge metric failed: {str(e)}")
        return 0.0


