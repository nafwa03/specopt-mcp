def generate_validation_markdown(file_path, original_prompt, optimized_instruction, history, dataset_size,
                                 optimizer_type="mipro"):
    """Generates a text-based dashboard report with normalized percentage tracking."""
    optimizer_label = optimizer_type.upper() if optimizer_type.lower() in ("mipro", "gepa") else optimizer_type
    raw_baseline = history[0]
    raw_optimized = history[1]

    if raw_baseline > 1.0:
        raw_baseline = raw_baseline / dataset_size
    if raw_optimized > 1.0:
        raw_optimized = raw_optimized / dataset_size

    baseline_pct = min(100.0, raw_baseline * 100.0)
    optimized_pct = min(100.0, raw_optimized * 100.0)
    improvement = optimized_pct - baseline_pct

    baseline_bars = int(baseline_pct // 5)
    optimized_bars = int(optimized_pct // 5)

    report = f"""# {optimizer_label} Optimization Performance Report

## Executive Summary
* **Target File Profile:** `{os.path.basename(file_path)}`
* **Synthetic Evaluation Dataset Size:** {dataset_size} custom scenarios
* **Initial Baseline Performance Score:** `{baseline_pct:.2f}%`
* **Final Optimized Performance Score:** `{optimized_pct:.2f}%`
* **Net Performance Shift:** `{"+" if improvement >= 0 else ""}{improvement:.2f}%`

## Performance Visualization
```text
Baseline Score:  [{'#' * baseline_bars}{'-' * (20 - baseline_bars)}] {baseline_pct:.1f}%
Optimized Score: [{'#' * optimized_bars}{'-' * (20 - optimized_bars)}] {optimized_pct:.1f}%
```

## Compiled Optimized Instructions
Below is the system prompt generated by the {optimizer_label} optimizer to direct your agent:
```text
{optimized_instruction}
```

---
*Report generated successfully on background worker thread.*
"""
    return report


