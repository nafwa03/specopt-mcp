def run_optimization_pipeline(agent_markdown_path: str, provider: str = "lm-studio", model: str = "",
                              lm_client=None, registry=None, optimizer_type: str = "mipro",
                              document_dir: str = "") -> str:
    """Run optimization on an agent markdown file using the selected optimizer (MIPROv2 or GEPA).
    Optionally accepts a document_dir for supplementary reference documentation.
    When docs are provided, the dataset is auto-validated before optimization."""
    if not os.path.exists(agent_markdown_path):
        return f"Error: Target file not found at path: {agent_markdown_path}"

    logger.info("Compiling local configurations...")

    with open(agent_markdown_path, "r", encoding="utf-8") as f:
        original_prompt = f.read()

    backup_path = f"{agent_markdown_path}{ConfigLoader.get("file_suffixes", "backup")}"
    with open(backup_path, "w", encoding="utf-8") as f:
        f.write(original_prompt)

    if lm_client is None:
        lm_client = _get_lm_client(provider, model, registry)

    prompt_body = original_prompt
    frontmatter_match = re.match(r"^(---\s*\n[\s\S]*?\n---\s*\n)", original_prompt)
    if frontmatter_match:
        prompt_body = original_prompt[len(frontmatter_match.group(1)):]

    current_instructions = prompt_body.strip()
    logger.info(f"Read current prompt text body ({len(current_instructions)} chars).")

    supplementary_docs = scan_document_directory(document_dir) if document_dir else ""
    if supplementary_docs:
        logger.info("Loaded supplementary docs (%d chars) from: %s", len(supplementary_docs), document_dir)

    with dspy.context(lm=lm_client):
        trainset = generate_dataset_via_lm(
            lm_client=lm_client,
            agent_prompt=current_instructions,
            num_examples=ConfigLoader.get("pipeline", "dataset", "num_examples"),
            supplementary_docs=supplementary_docs,
        )

        generated_dataset_path = os.path.splitext(agent_markdown_path)[0] + ConfigLoader.get("file_suffixes", "generated_dataset")
        try:
            serializable = [
                {
                    "input_context": ex.input_context,
                    "gold_response": getattr(ex, "output_response", getattr(ex, "gold_response", "")),
                    "source_context": supplementary_docs,
                }
                for ex in trainset
            ]
            with open(generated_dataset_path, "w") as f:
                json.dump(serializable, f, indent=2)
            logger.info("Generated dataset saved to %s (%d examples)", generated_dataset_path, len(trainset))
        except Exception as e:
            logger.warning("Failed to save generated dataset: %s", e)

        # Auto-validate dataset before running the optimizer
        val_report = ""
        try:
            val_cfg = ConfigLoader.get("pipeline", "dataset_validation")
        except KeyError:
            val_cfg = {"enabled": True}
        if val_cfg.get("enabled", True):
            logger.info("Running automatic dataset validation...")
            try:
                val_report = run_dataset_validation_pipeline(
                    dataset_path=generated_dataset_path,
                    agent_markdown_path=agent_markdown_path,
                    provider=provider,
                    model=model,
                    lm_client=lm_client,
                    registry=registry,
                )
                logger.info("Dataset validation complete:\n%s", val_report)
                if "FAIL" in val_report and val_cfg.get("on_failure", "warn") == "abort":
                    return f"Dataset validation FAILED. Aborting optimization.\n\n{val_report}"
            except Exception as val_err:
                logger.warning("Auto-validation failed (continuing): %s", val_err)

        customized_signature = DynamicAgentSignature.with_instructions(current_instructions)
        base_program = dspy.Predict(customized_signature)

        evaluator = Evaluate(devset=trainset, metric=secure_universal_llm_metric, display_progress=False)
        logger.info("Phase: Baseline evaluation starting (%d examples)...", len(trainset))
        baseline_result = evaluator(base_program)
        baseline_score = float(getattr(baseline_result, 'score', baseline_result))
        logger.info(f"Phase: Baseline Score: {baseline_score:.1f}%")

        teleprompter = _create_teleprompter(
            optimizer_type=optimizer_type,
            metric=secure_universal_llm_metric,
            lm_client=lm_client,
            task_model=lm_client,
        )
        compile_kwargs = dict(trainset=trainset, valset=trainset)
        if optimizer_type.lower().strip() == "mipro":
            compile_kwargs.update(
                minibatch=ConfigLoader.get("pipeline", "mipro", "minibatch"),
                num_trials=ConfigLoader.get("pipeline", "mipro", "num_trials"),
                max_bootstrapped_demos=ConfigLoader.get("pipeline", "mipro", "max_bootstrapped_demos"),
            )
        logger.info("Phase: %s compile starting...", optimizer_type.upper())
        try:
            compiled_program = teleprompter.compile(base_program.deepcopy(), **compile_kwargs)
        except Exception as e:
            if "ellipsis" in str(e).lower():
                return f"GEPA optimization failed due to an LM output parsing issue: {e}"
            raise
        logger.info("Phase: %s compile complete.", optimizer_type.upper())

        logger.info("Phase: Optimized evaluation starting...")
        optimized_result = evaluator(compiled_program)
        optimized_score = float(getattr(optimized_result, 'score', optimized_result))
        logger.info(f"Phase: Optimized Score: {optimized_score:.1f}%")

        logger.info("Phase: Extracting optimized instructions...")
        optimized_instructions = _extract_optimized_instructions(teleprompter, compiled_program, optimizer_type)
        logger.info("Phase: Extracted instruction (%d chars). Generating report...", len(optimized_instructions))
        report = generate_validation_markdown(
            file_path=agent_markdown_path,
            original_prompt=original_prompt,
            optimized_instruction=optimized_instructions,
            history=[baseline_score, optimized_score],
            dataset_size=len(trainset),
            optimizer_type=optimizer_type,
        )
        logger.info("Phase: Report generated.")

    try:
        report_dir = os.path.dirname(os.path.abspath(agent_markdown_path))
        report_file = os.path.join(report_dir, ConfigLoader.get("file_suffixes", "report"))
        logger.info("Phase: Writing report to %s", report_file)
        with open(report_file, "w", encoding="utf-8") as f:
            f.write(report)
        logger.info(f"Report saved to {report_file}")
    except Exception as report_error:
        logger.error(f"Failed to write report to disk: {str(report_error)}")

    if optimized_score >= baseline_score:
        logger.info("Optimized text beat or matched baseline. Updating file layout...")
        optimized_demos = _extract_optimized_demos(compiled_program, optimizer_type)
        if registry:
            file_skill = registry.get_skill("surgical_file_modifier")
            file_skill.execute(
                file_path=agent_markdown_path,
                new_prompt=optimized_instructions,
                demos=optimized_demos
            )
        else:
            compiled_prompt_path = os.path.splitext(agent_markdown_path)[0] + ConfigLoader.get("file_suffixes", "compiled_prompt")
            demo_blocks = []
            for idx, demo in enumerate(optimized_demos):
                ctx = demo.get('input_context', demo.get('input', ''))
                resp = demo.get('output_response', demo.get('output', ''))
                if ctx and resp:
                    demo_blocks.append(f"### Example Case {idx + 1}\nInput: {ctx}\nOutput: {resp}\n")
            compiled_content = f"# COMPILED AGENT SYSTEM INSTRUCTIONS\n{optimized_instructions}\n\n## Bootstrapped Few-Shot Demonstrations\n" + (
                "\n".join(demo_blocks) if demo_blocks else "No high-scoring demos isolated.")
            with open(compiled_prompt_path, "w", encoding="utf-8") as f:
                f.write(compiled_content.strip())
        val_prefix = f"\n--- Dataset Validation ---\n{val_report}\n" if val_report else ""
        return f"{val_prefix}Success! Baseline: {baseline_score:.0f}%, Optimized: {optimized_score:.0f}%."
    else:
        logger.info("Proposed changes didn't beat the baseline. File left untouched.")
        val_prefix = f"\n--- Dataset Validation ---\n{val_report}\n" if val_report else ""
        return f"{val_prefix}Existing layout scored higher ({baseline_score:.0f}%) than proposed variations. File left untouched."


def run_specification_pipeline(spec_json_path: str, provider: str = "lm-studio", model: str = "",
                               lm_client=None, registry=None) -> str:
    """Optimize JSON schema documentation fields using DSPy while preserving structure."""
    if not os.path.exists(spec_json_path):
        return f"Error: Target file not found at path: {spec_json_path}"

    try:
        if lm_client is None:
            lm_client = _get_lm_client(provider, model, registry)

        if lm_client is None:
            lm_client = _get_lm_client(provider, model, registry)

        with dspy.context(lm=lm_client):
            optimizer_program = dspy.Predict(SpecDocumentationSignature)

            def dspy_optimization_worker(text_to_optimize: str) -> str:
                response = optimizer_program(raw_description=text_to_optimize)
                return response.optimized_description.strip()

            if registry:
                spec_skill = registry.get_skill("spec_structural_isolation")
                spec_skill.execute(file_path=spec_json_path, optimize_callback=dspy_optimization_worker)
            else:
                with open(spec_json_path, "r", encoding="utf-8") as f:
                    spec_data = json.load(f)

                def walk_and_optimize(node):
                    if isinstance(node, dict):
                        new_dict = {}
                        for key, value in node.items():
                            if key in ["description", "summary", "title"] and isinstance(value, str) and value.strip():
                                new_dict[key] = dspy_optimization_worker(value)
                            else:
                                new_dict[key] = walk_and_optimize(value)
                        return new_dict
                    elif isinstance(node, list):
                        return [walk_and_optimize(item) for item in node]
                    return node

                optimized_spec = walk_and_optimize(spec_data)
                base, ext = os.path.splitext(spec_json_path)
                optimized_file_path = f"{base}{ConfigLoader.get("file_suffixes", "optimized")}{ext}"
                with open(optimized_file_path, "w", encoding="utf-8") as f:
                    json.dump(optimized_spec, f, indent=2)

        return f"Success! Specification metadata at '{spec_json_path}' optimized successfully."
    except Exception as e:
        logger.exception("Specification optimization encountered a failure:")
        return f"Specification optimization failed: {str(e)}"


def run_code_optimization_pipeline(skill_file_path: str, provider: str = "lm-studio", model: str = "",
                                   lm_client=None, registry=None, optimizer_type: str = "mipro") -> str:
    """Optimize Python skill code via MIPROv2 or GEPA with subprocess pytest metric validation."""
    if not os.path.exists(skill_file_path):
        return f"Error: Target file not found at path: {skill_file_path}"
    if not skill_file_path.endswith(".py"):
        return f"Error: Can only optimize valid Python (.py) source files."

    try:
        if lm_client is None:
            lm_client = _get_lm_client(provider, model, registry)

        with open(skill_file_path, "r", encoding="utf-8") as f:
            current_source_code = f.read()

        code_example = dspy.Example(input_context=current_source_code, output_response="PASS").with_inputs(
            "input_context")
        mock_code_set = [code_example]

        def dynamic_pytest_reward_metric(gold, pred, trace=None):
            clean_code = pred.optimized_python_code.strip()
            if clean_code.startswith("```python"):
                clean_code = re.sub(r"^```python|```$", "", clean_code, flags=re.MULTILINE).strip()
            elif clean_code.startswith("```"):
                clean_code = re.sub(r"^```|```$", "", clean_code, flags=re.MULTILINE).strip()

            temp_test_file = skill_file_path + ".tmp"
            backup_file = skill_file_path + ".bak"

            try:
                with open(temp_test_file, "w", encoding="utf-8") as f:
                    f.write(clean_code)

                syntax_check = subprocess.run(
                    [sys.executable, "-m", "py_compile", temp_test_file],
                    capture_output=True
                )
                if syntax_check.returncode != 0:
                    return 0.0

                shutil.copyfile(skill_file_path, backup_file)
                shutil.copyfile(temp_test_file, skill_file_path)

                test_suite_run = subprocess.run(
                    ["pytest", "tests/test_skills.py"],
                    capture_output=True,
                    text=True
                )

                shutil.copyfile(backup_file, skill_file_path)

                if os.path.exists(temp_test_file):
                    os.remove(temp_test_file)
                if os.path.exists(backup_file):
                    os.remove(backup_file)

                if test_suite_run.returncode == 0:
                    return 1.0
                else:
                    return 0.0

            except Exception as e:
                logger.error(f"Metric failure: {str(e)}")
                if os.path.exists(backup_file):
                    shutil.copyfile(backup_file, skill_file_path)
                for f_path in [temp_test_file, backup_file]:
                    if os.path.exists(f_path):
                        os.remove(f_path)
                return 0.0

        with dspy.context(lm=lm_client):
            code_program = dspy.Predict(CodeOptimizationSignature)

            teleprompter = _create_teleprompter(
                optimizer_type=optimizer_type,
                metric=dynamic_pytest_reward_metric,
                lm_client=lm_client,
                task_model=lm_client,
                num_candidates=ConfigLoader.get("pipeline", "mipro", "num_candidates_code"),
            )
            compile_kwargs = dict(trainset=mock_code_set, valset=mock_code_set)
            if optimizer_type.lower().strip() == "mipro":
                compile_kwargs.update(
                    minibatch=ConfigLoader.get("pipeline", "mipro", "minibatch"),
                    num_trials=ConfigLoader.get("pipeline", "mipro", "num_trials_code"),
                    max_bootstrapped_demos=ConfigLoader.get("pipeline", "mipro", "max_bootstrapped_demos_code"),
                )
            compiled_code_program = teleprompter.compile(code_program, **compile_kwargs)

            final_optimized_code = compiled_code_program.optimized_python_code.strip()

        if final_optimized_code.startswith("```python"):
            final_optimized_code = re.sub(r"^```python|```$", "", final_optimized_code, flags=re.MULTILINE).strip()
        elif final_optimized_code.startswith("```"):
            final_optimized_code = re.sub(r"^```|```$", "", final_optimized_code, flags=re.MULTILINE).strip()

        if len(final_optimized_code) < 50:
            return "Optimizer returned an uncharacteristically short code string. Overwrite prevented."

        with open(skill_file_path, "w", encoding="utf-8") as f:
            f.write(final_optimized_code)

        return f"Success! '{skill_file_path}' has been autonomously optimized, security-vetted, and re-written."

    except Exception as e:
        return f"Code optimization pipeline failed: {str(e)}"


