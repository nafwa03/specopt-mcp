def extract_and_load_dataset(raw_output: str) -> list:
    """Parse LLM JSON output into a list of dspy.Example objects with fallback handling."""
    json_clean = re.sub(r"^```json|```$", "", raw_output, flags=re.MULTILINE).strip()

    try:
        raw_json_data = json.loads(json_clean)
    except Exception:
        try:
            raw_json_data = ast.literal_eval(json_clean)
        except Exception:
            array_match = re.search(r"(\[\s*\{[\s\S]*?\}\s*\])", raw_output)
            if array_match:
                try:
                    raw_json_data = ast.literal_eval(array_match.group(1))
                except Exception:
                    raw_json_data = []
            else:
                raw_json_data = []

    if not raw_json_data or not isinstance(raw_json_data, list):
        raw_json_data = [
            {"input_context": "Write a short creative stanza.",
             "gold_response": "The gears turn quietly inside the machine."},
            {"input_context": "Draft a short poetic line.",
             "gold_response": "Neon reflections dancing in wet asphalt lanes."}
        ]
    return [
        dspy.Example(input_context=i.get("input_context", ""), output_response=i.get("gold_response", "")).with_inputs(
            "input_context") for i in raw_json_data]


def chunk_text(text: str, chunk_size: int = 4000, chunk_overlap: int = 200) -> list:
    """Split text into overlapping chunks for processing large documents."""
    if not text or len(text) <= chunk_size:
        return [text] if text else []
    chunks = []
    start = 0
    while start < len(text):
        end = min(start + chunk_size, len(text))
        chunks.append(text[start:end])
        start += chunk_size - chunk_overlap
    return chunks


def generate_dataset_via_lm(
    lm_client,
    agent_prompt: str,
    num_examples: int = 5,
    supplementary_docs: str = "",
    chunk_size: int = 0,
    chunk_overlap: int = 200,
) -> list:
    """Generate a synthetic dataset via direct LM call, with automatic chunking
    for large supplementary documents."""
    if not supplementary_docs:
        return _generate_single(lm_client, agent_prompt, num_examples, "")

    effective_chunk_size = chunk_size if chunk_size > 0 else ConfigLoader.get("pipeline", "dataset", "chunk_size")
    chunks = chunk_text(supplementary_docs, effective_chunk_size, chunk_overlap)

    if len(chunks) <= 1:
        return _generate_single(lm_client, agent_prompt, num_examples, supplementary_docs)

    per_chunk = max(1, num_examples // len(chunks))
    if per_chunk < 2:
        logger.warning("Too many chunks (%d) for %d examples; falling back to single-shot generation", len(chunks), num_examples)
        return _generate_single(lm_client, agent_prompt, num_examples, supplementary_docs[:effective_chunk_size])

    logger.info("Splitting supplementary docs into %d chunks (size=%d, overlap=%d)", len(chunks), effective_chunk_size, chunk_overlap)
    all_examples = []

    for i, chunk in enumerate(chunks):
        logger.info("Generating examples from chunk %d/%d...", i + 1, len(chunks))
        try:
            chunk_examples = _generate_single(lm_client, agent_prompt, per_chunk, chunk)
            all_examples.extend(chunk_examples)
        except Exception as e:
            logger.warning("Chunk %d generation failed: %s", i + 1, e)
            continue

    if len(all_examples) < num_examples:
        remaining = num_examples - len(all_examples)
        try:
            extra = _generate_single(lm_client, agent_prompt, remaining, supplementary_docs[:effective_chunk_size])
            all_examples.extend(extra)
        except Exception as e:
            logger.warning("Fallback generation failed: %s", e)

    return all_examples[:num_examples]


def _generate_single(lm_client, agent_prompt: str, num_examples: int, doc_text: str = "") -> list:
    """Single-shot dataset generation from a prompt + optional doc text."""
    doc_block = f"\nReference documentation:\n{doc_text}\n" if doc_text else ""
    prompt = (
        f"Generate exactly {num_examples} realistic test scenarios for the following agent prompt.\n"
        f"\nAGENT SYSTEM PROMPT:\n{agent_prompt}\n"
        f"{doc_block}"
        f"\nFor each scenario, provide:\n"
        f'- "input_context": A realistic user query the agent might receive\n'
        f'- "gold_response": The correct answer the agent should return\n'
        f"\nOutput ONLY a raw JSON array of objects with keys 'input_context' and 'gold_response'. "
         f"No markdown, no emojis, no explanations.\n\nJSON ARRAY:"
    )
    raw = lm_client(prompt=prompt)
    raw_text = raw[0] if isinstance(raw, list) else str(raw)
    return extract_and_load_dataset(raw_text)


def curate_dataset(lm_client, examples: list, threshold: float = 7.0, max_examples: int = 0) -> list:
    """Score each example 1-10 using LLM-as-judge, filter below threshold.
    Returns curated examples sorted by score descending.
    If max_examples > 0, returns at most that many top examples."""
    scored = []
    for i, ex in enumerate(examples):
        score = _score_example_quality(lm_client, ex)
        logger.info("Curated example %d/%d: score=%.1f", i + 1, len(examples), score)
        if score >= threshold:
            scored.append((score, ex))

    scored.sort(key=lambda pair: pair[0], reverse=True)
    curated = [ex for _, ex in scored]
    if max_examples > 0 and len(curated) > max_examples:
        curated = curated[:max_examples]
    logger.info("Curated dataset: %d/%d examples passed threshold=%.1f", len(curated), len(examples), threshold)
    return curated


def _score_example_quality(lm_client, example) -> float:
    """Score a single training example 1-10 using LLM-as-judge."""
    prompt = (
        "Rate the quality of this training example from 1 (worst) to 10 (best).\n\n"
        f"Input: {example.input_context}\n"
        f"Expected Output: {getattr(example, 'output_response', getattr(example, 'gold_response', ''))}\n\n"
        "Criteria:\n"
        "- Is the input a realistic user query for the agent?\n"
        "- Is the expected output correct and specific?\n"
        "- Is the pair detailed enough to train on?\n\n"
        "Return ONLY a single number between 1 and 10. No other text."
    )
    try:
        raw = lm_client(prompt=prompt, temperature=0.0, max_tokens=10)
        text = raw[0] if isinstance(raw, list) else str(raw)
        match = re.search(r"(\d+(?:\.\d+)?)", text)
        if match:
            return float(match.group(1))
    except Exception as e:
        logger.warning("Quality scoring failed for example: %s", e)
    return 0.0


def convert_dataset_format(examples: list, fmt: str = "json", supplementary_docs: str = "") -> str:
    """Convert a list of dspy.Examples to the specified output format.
    Supported formats: json, jsonl, alpaca, chatml."""
    serializable = [
        {
            "input_context": ex.input_context,
            "gold_response": getattr(ex, "output_response", getattr(ex, "gold_response", "")),
            "source_context": supplementary_docs or getattr(ex, "source_context", ""),
        }
        for ex in examples
    ]

    if fmt == "json":
        return json.dumps(serializable, indent=2)

    elif fmt == "jsonl":
        return "\n".join(json.dumps(item) for item in serializable)

    elif fmt == "alpaca":
        alpaca_items = []
        for item in serializable:
            alpaca_items.append({
                "instruction": "Respond to the user query based on the agent's system prompt.",
                "input": item["input_context"],
                "output": item["gold_response"],
            })
        return json.dumps(alpaca_items, indent=2)

    elif fmt == "chatml":
        chatml_items = []
        for item in serializable:
            chatml_items.append({
                "messages": [
                    {"role": "user", "content": item["input_context"]},
                    {"role": "assistant", "content": item["gold_response"]},
                ]
            })
        return json.dumps(chatml_items, indent=2)

    else:
        logger.warning("Unknown output format '%s', falling back to json", fmt)
        return json.dumps(serializable, indent=2)


def read_document_file(file_path: str) -> str:
    """Read a document file and return its text content.
    Supports .md, .txt, .json, .pdf, .docx, .html, .pptx."""
    ext = os.path.splitext(file_path)[1].lower()
    try:
        if ext == ".pdf":
            if fitz is None:
                logger.warning("PyMuPDF not installed, skipping PDF: %s", file_path)
                return ""
            doc = fitz.open(file_path)
            text = "\n".join(page.get_text() for page in doc)
            doc.close()
            return text.strip()

        elif ext == ".json":
            with open(file_path, "r", encoding="utf-8") as f:
                data = json.load(f)
            return json.dumps(data, indent=2) if isinstance(data, dict) else json.dumps(data, indent=2)

        elif ext == ".docx":
            if DocxDocument is None:
                logger.warning("python-docx not installed, skipping: %s", file_path)
                return ""
            doc = DocxDocument(file_path)
            return "\n".join(p.text for p in doc.paragraphs).strip()

        elif ext == ".html":
            if BeautifulSoup is None:
                logger.warning("beautifulsoup4 not installed, skipping: %s", file_path)
                return ""
            with open(file_path, "r", encoding="utf-8") as f:
                soup = BeautifulSoup(f.read(), "html.parser")
            for tag in soup(["script", "style", "nav", "footer", "header"]):
                tag.decompose()
            return soup.get_text(separator="\n").strip()

        elif ext == ".pptx":
            if Presentation is None:
                logger.warning("python-pptx not installed, skipping: %s", file_path)
                return ""
            prs = Presentation(file_path)
            lines = []
            for slide in prs.slides:
                for shape in slide.shapes:
                    if hasattr(shape, "text"):
                        lines.append(shape.text)
            return "\n".join(lines).strip()

        else:
            with open(file_path, "r", encoding="utf-8") as f:
                return f.read().strip()

    except Exception as e:
        logger.warning("Failed to read document %s: %s", file_path, e)
        return ""


def scan_document_directory(document_dir: str) -> str:
    """Scan a flat directory for supported documents and return their concatenated text.
    Supports .md, .txt, .json, .pdf extensions (non-recursive)."""
    if not document_dir or not os.path.isdir(document_dir):
        return ""

    extensions = ConfigLoader.get("pipeline", "dataset", "document_extensions")
    pieces = []

    for filename in sorted(os.listdir(document_dir)):
        filepath = os.path.join(document_dir, filename)
        if not os.path.isfile(filepath):
            continue
        ext = os.path.splitext(filename)[1].lower()
        if ext in extensions:
            content = read_document_file(filepath)
            if content:
                pieces.append(f"--- {filename} ---\n{content}")

    if not pieces:
        return ""

    return "\n\n".join(pieces)


def extract_markdown_sections(file_path: str) -> dict:
    """Splits a markdown document by its top level or secondary headers."""
    with open(file_path, "r", encoding="utf-8") as f:
        content = f.read()

    sections = {}
    current_header = "General Overview"
    current_text = []

    for line in content.splitlines():
        if line.startswith("#"):
            if current_text:
                sections[current_header] = "\n".join(current_text).strip()
            current_header = line.lstrip("# ").strip()
            current_text = []
        else:
            current_text.append(line)

    if current_text:
        sections[current_header] = "\n".join(current_text).strip()

    return sections


def generate_dynamic_dataset(agent_file_path: str, count: int = 15, lm_client=None) -> list:
    """Extracts sections from markdown and builds targeted synthetic evaluation splits."""
    logger.info(f"Extracting sub-sections from agent profile: {agent_file_path}")

    sections = extract_markdown_sections(agent_file_path)
    dspy_dataset = []

    examples_per_section = max(2, count // len(sections))

    min_body_length = ConfigLoader.get("pipeline", "section", "min_body_length")
    for header, body in sections.items():
        if len(body) < min_body_length:
            continue

        logger.info(f"Generating synthetic cases for documentation segment: [{header}]")
        context_prompt = f"Document Section: {header}\nContent Details:\n{body}"

        try:
            section_examples = generate_dataset_via_lm(
                lm_client=lm_client,
                agent_prompt=context_prompt,
                num_examples=examples_per_section,
            )
            dspy_dataset.extend(section_examples)

        except Exception as e:
            logger.warning(f"Failed to generate specific rows for section {header}: {str(e)}")
            continue

    if not dspy_dataset:
        logger.error("All structural parsing loops failed. Injecting safe baseline dataset.")
        fallback_data = [
            {"input_context": "How do I run tracking on bears and giraffes?",
             "gold_response": "Run python main.py --class-id 21,23"},
            {"input_context": "Can I edit main2.py to add features?",
             "gold_response": "No, main2.py is frozen. All active edits must happen inside main.py."}
        ]
        for item in fallback_data:
            dspy_dataset.append(dspy.Example(**item).with_inputs("input_context"))

    logger.info(f"Total structured synthetic dataset generated successfully: {len(dspy_dataset)} rows.")
    return dspy_dataset


